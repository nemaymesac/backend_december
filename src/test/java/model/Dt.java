package model;

public class Dt {
    private short dt;
    private short id;
    private String name;
    private int cod;

    public short getDt() {
        return dt;
    }

    public void setDt(short dt) {
        this.dt = dt;
    }

    public short getId() {
        return id;
    }

    public void setId(short id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCod() {
        return cod;
    }

    public void setCod(int cod) {
        this.cod = cod;
    }

}
